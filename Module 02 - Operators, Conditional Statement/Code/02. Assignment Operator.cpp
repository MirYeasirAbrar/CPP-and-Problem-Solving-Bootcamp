 // += , -+, *=, /= 
 #include <iostream>
 using namespace std;

 int main()
 {
    int a = 5;
    a = a + 2;
    cout << a <<'\n';
    a += 2;
    cout << a <<'\n';
    a = a - 3;
    cout << a <<'\n';
    a -= 3;
    cout << a <<'\n';
    a = a * 2;
    cout << a <<'\n';
    a *= 2;
    cout << a <<'\n';
    a = a / 5;
    cout << a <<'\n';
    a /= 5;
    cout << a <<'\n';
    
    return 0;
 }